    private void goButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_goButtonActionPerformed
        try {
            Usuario seleccionado = (Usuario) this.jList1.getSelectedValue();
            VerPerfil vista = new VerPerfil(this, this.controlador.getPersistencia(), (Usuario) this.jList1.getSelectedValue(), 1);
            if (this.usuario.getId().equals(seleccionado.getId())) {
                vista = new VerPerfil(this, this.controlador.getPersistencia(), (Usuario) this.jList1.getSelectedValue(), 0);
            }
            vista.setVisible(true);
          
            vista.setLocationRelativeTo(null);
            this.dispose();
        } catch (Exception e) {
            //error handling code
        }

        try {
            Pregunta _preg = (Pregunta) this.jList1.getSelectedValue();
            ViewPregunta vista = new ViewPregunta(this, (Pregunta) this.jList1.getSelectedValue(), _preg.getForo(), this.controlador, this.usuario);
          
            vista.setLocationRelativeTo(null);
            vista.setVisible(true);
            this.dispose();
        } catch (Exception e) {
            //error handling code
        }

        try {
            ViewForo foro = new ViewForo(this, (Foro) this.jList1.getSelectedValue(), this.controlador, this.usuario);
            foro.setVisible(true);
           
            foro.setLocationRelativeTo(null);
            this.dispose();
        } catch (Exception e) {
            //error handling code
        }


    }//GEN-LAST:event_goButtonActionPerformed


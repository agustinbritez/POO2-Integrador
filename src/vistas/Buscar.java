    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        if (this.jTextField1.getText().length() >= 2) {
            if (combo.getSelectedItem().toString() == "USUARIOS") {
                buscarUsuario();
            } else if (combo.getSelectedItem().toString() == "FOROS") {
                buscarForo();
            } else if (combo.getSelectedItem().toString() == "PREGUNTAS") {
                buscarPregunta();
            } else if (combo.getSelectedItem().toString() == "TODO") {
                buscarTodo();
            }
        }
        if (this.jTextField1.getText().length() < 2) {
            if (combo.getSelectedItem().toString() == "USUARIOS") {
                cargarUsuario();
            } else if (combo.getSelectedItem().toString() == "FOROS") {
                cargarForo();
            } else if (combo.getSelectedItem().toString() == "PREGUNTAS") {
                cargarPregunta();
            } else if (combo.getSelectedItem().toString() == "TODO") {
                cargarTodo();
            }
        }

    }//GEN-LAST:event_jTextField1KeyTyped


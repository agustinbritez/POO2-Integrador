    private void listDetalleValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listDetalleValueChanged
        if (this.listDetalle.getSelectedIndex() == 1) {
            if (this.listPreguntas.getSelectedValue() != null) {
                VerPerfil perfil = new VerPerfil(this, this.controlador.getPersistencia(), this.listPreguntas.getSelectedValue().getUsuario(), 1);
                perfil.setVisible(true);
                perfil.setLocationRelativeTo(null);
                perfil.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();

            }

        }
    }//GEN-LAST:event_listDetalleValueChanged

    public void cargarListPreguntas() {
        DefaultListModel modelo = new DefaultListModel();
        Arrays.sort(this.controlador.obtenerPreguntasDelForo(foro).toArray());
        for (Pregunta pre : this.controlador.obtenerPreguntasDelForo(foro)) {
            modelo.addElement(pre);
        }
        this.listPreguntas.setModel(modelo);
    }

    public void cargarListaDetalle() {
        DefaultListModel modelo = new DefaultListModel();
        List lista = this.controlador.obtenerInformacionPregunta(this.listPreguntas.getSelectedValue());
        try {
            modelo.add(0, "Usuario: " + lista.get(0));
            modelo.add(1, "Tipo de usuario: " + lista.get(1));
            modelo.add(2, "Titulo: " + lista.get(2));
            modelo.add(3, "Fecha de publicacion: " + lista.get(4));
            modelo.add(4, "Cantidad de respuestas : " + lista.get(5));
            modelo.add(5, "Fecha de la ultima respuesta : " + lista.get(6));
        } catch (Exception ex) {
        }
        this.listDetalle.setModel(modelo);
    }
